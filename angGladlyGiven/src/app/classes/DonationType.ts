/**
 * Enumeration representing different types of donations.
 */

export enum DonationType {

    /**
   * Represents a singular donation.
   */
    Singular = 'Singular',

    /**
   * Represents a monthly recurring donation.
   */

    Monthly = 'Monthly',

    /**
   * Represents a yearly recurring donation.
   */

    Yearly = 'Yearly'
}
